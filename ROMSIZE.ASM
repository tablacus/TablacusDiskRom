START	EQU	0B000H
DATA	EQU	START+00100H

BDOS	EQU	0F37DH

	ORG	START-7

;	MSX BINARY HEADER
	DB	0FEH
;	MSX BINARY START ADDRESS
	DW	START
;	MSX BINARY END ADDRESS
	DW	LAST_ADR-1
;	MSX BINARY EXEC ADDRESS
	DW	START

	XOR	A
	LD	DE,DATA
	LD	(DE),A
	LD	C,01AH		;DTAの設定(_SETDTA)
	CALL	BDOS

	LD	L,1		;ドライブ番号 Bドライブ
	LD	H,18		;読みだすセクタ数9KB
	LD	DE,0		;セクタ番号
	LD	C,02FH		;論理セクタを用いた読み出し(_RDABS)
	JP	BDOS
LAST_ADR: